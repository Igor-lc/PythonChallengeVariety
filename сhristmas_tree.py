def generate(numRows):
    lists = []

    if numRows == 1:
        return [[1]]
    elif numRows == 2:
        return [[1], [1,1]]
    elif numRows > 2:
        lists = [[1], [1,1]]

        for row in range(1, numRows - 1):
            last_list = lists[-1].copy()
            new_list = [1]

            for i in range(len(last_list) - 1):
                new_list.append(last_list[i] + last_list[i + 1])
            new_list.append(1)

            lists += [new_list]

    length = len(lists[-1]) * 10
    for list in lists:
        print(str('   '.join(map(str, list))).center(length, '.'))


generate(10)

